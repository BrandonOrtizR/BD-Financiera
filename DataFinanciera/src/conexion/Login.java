    //Configuracion de logo
    private void pinterImage(JLabel lbl,String ruta){
            
            this.imagen =new ImageIcon(ruta);
            this.icon = new ImageIcon(
                    this.imagen.getImage().getScaledInstance(
                            lbl.getWidth(), 
                            lbl.getHeight(),
                            Image.SCALE_SMOOTH
                    )
            );
            lbl.setIcon(this.icon);
            this.repaint();
    }
    
